#include "../minishell.h"

int	ft_dlstsize(t_dlist *lst)
{
	int	i;

	i = 0;
	if (lst)
	{
		i++;
		while (lst->next != 0)
		{
			lst = lst->next;
			i++;
		}
	}
	return (i);
}
